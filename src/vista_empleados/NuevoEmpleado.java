    private void botonAceptarActionPerformed(java.awt.event.ActionEvent evt) {
 	   
    	
    	if(botonRadioTiempoCompleto.isSelected())
    	{
    		
    		EmpleadoTiempoCompleto  empleado = new EmpleadoTiempoCompleto(campoDNI.getText(),campoNombreApellidos.getText(),Double.valueOf(campoSalario.getText()).doubleValue(),campoDepartamento.getText(),campoContrato.getText());
    		  empleado.setActivo(true);
    	        ControladorAplicacionImp.obtenerInstancia().accion(EventoNegocio.NUEVO_EMPLEADO, empleado);
    	}
    	else
    	{
    		
    		EmpleadoTiempoParcial empleado = new EmpleadoTiempoParcial(campoDNI.getText(),campoNombreApellidos.getText(),Double.valueOf(campoSalario.getText()).doubleValue(),campoDepartamento.getText(),campoCorreo.getText());
    		  empleado.setActivo(true);
    	        ControladorAplicacionImp.obtenerInstancia().accion(EventoNegocio.NUEVO_EMPLEADO, empleado);
    	}
 	  
 	
    }
    
