    @Override
	public void actualizar(int id_evento_vista, Object datos) {
	   if(id_evento_vista == EventoVista.NUEVO_EMPLEADO_EXITO){
			JOptionPane.showMessageDialog(null, "Se ha creado el empleado con éxito con id " + (int)datos, "Creación Empleado", JOptionPane.INFORMATION_MESSAGE);
	   }
	   else if(id_evento_vista == EventoVista.NUEVO_EMPLEADO_DEPARTAMENTO_NO_EXISTE){
			JOptionPane.showMessageDialog(null, "ERROR!! El departamento no existe", "Creación Empleado", JOptionPane.ERROR_MESSAGE);
		}
	   else{
			JOptionPane.showMessageDialog(null, "ERROR!! El empleado no se ha creado", "Creación Empleado", JOptionPane.ERROR_MESSAGE);
	   }
	   
   }
