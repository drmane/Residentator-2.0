    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {
      TransferFactura transferFactura = new TransferFactura();
      
      transferFactura.setId(Integer.valueOf(jFormattedTextField1.getText()).intValue());
 	   
 	  transferFactura.setCutoa(Double.valueOf(jFormattedTextField3.getText()).doubleValue());
 	   
 	 transferFactura.setEstaPagado(jRadioButton1.isSelected());
 	 transferFactura.setEnMetalico(jRadioButton2.isSelected());
 	 
 	 if(jFormattedTextField6.isEditable()){
 		transferFactura.setNumeroCuenta(jFormattedTextField6.getText());
 	 }
 	 else{
 		transferFactura.setNumeroCuenta("");
 	 }
 	   
      ControladorAplicacionImp.obtenerInstancia().accion(EventoNegocio.MODIFICAR_FACTURA, transferFactura);
    }

    private void jRadioButton2ActionPerformed(java.awt.event.ActionEvent evt) {                                              
        if(jRadioButton2.isSelected()){
            jFormattedTextField6.setEditable(false);
        }
        else{
            jFormattedTextField6.setEditable(true);
        }
    }                                             

