	@Override
	public void actualizar(int id_evento_vista, Object datos) {
		jFormattedTextField1.setText("");
		jFormattedTextField3.setText("");
		jFormattedTextField6.setText("");
		
		if(id_evento_vista == EventoVista.MODIFICAR_FACTURA_EXITO){
			JOptionPane.showMessageDialog(null, "Se ha modificado la factura con éxito ", "Modificar Factura", JOptionPane.INFORMATION_MESSAGE);		
		}	
		else if (id_evento_vista == EventoVista.MODIFICAR_FACTURA_NO_EXISTE){
			JOptionPane.showMessageDialog(null, "ERROR!! El ID de la factura introducida no existe", "Modificar Facturan", JOptionPane.ERROR_MESSAGE);
		}
		else if (id_evento_vista == EventoVista.MODIFICAR_FACTURA_FALLO){
			JOptionPane.showMessageDialog(null, "ERROR!! Ha ocurrido un error con la BD", "Modificar Factura", JOptionPane.ERROR_MESSAGE);
		}
	}
